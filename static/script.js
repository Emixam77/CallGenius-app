const orb = document.getElementById('orb');
const statusText = document.getElementById('status-text');
const callBtn = document.getElementById('call-btn');
const log = document.getElementById('log');
const setupView = document.getElementById('setup-view');
const orbView = document.getElementById('orb-view');
const setupForm = document.getElementById('setup-form');
const btnCreate = document.getElementById('btn-create');
const btnBack = document.getElementById('btn-back');

let isCalling = false;
let isSpeaking = false; // Flag to prevent overlaps
let sessionId = null; // Session ID generated by backend after setup

// --- Gestion du Formulaire d'Onboarding ---
setupForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    const originalText = btnCreate.innerHTML;
    btnCreate.innerHTML = '<span class="icon">⏳</span> Création du cerveau IA...';
    btnCreate.disabled = true;

    try {
        const formData = new FormData(setupForm);
        const response = await fetch('/api/setup', {
            method: 'POST',
            body: formData
        });
        
        const data = await response.json();
        
        if (data.success) {
            sessionId = data.session_id;
            console.log("IA Configurée ! Prompt généré.");
            
            // Switch views
            setupView.classList.replace('view-active', 'view-hidden');
            orbView.classList.replace('view-hidden', 'view-active');
            
            // Mise à jour du footer démo
            const companyName = formData.get('name') || "Entreprise";
            document.querySelector('footer p').innerText = `Démo : ${companyName}`;
        } else {
            alert("Erreur lors de la configuration: " + data.error);
        }
    } catch (e) {
        alert("Erreur de réseau. " + e);
    } finally {
        btnCreate.innerHTML = originalText;
        btnCreate.disabled = false;
    }
});

btnBack.addEventListener('click', () => {
    if (isCalling) endCall();
    orbView.classList.replace('view-active', 'view-hidden');
    setupView.classList.replace('view-hidden', 'view-active');
    log.innerHTML = '<div class="log-placeholder">L\'historique s\'affichera ici...</div>';
});

// 1. Initialiser la Synthèse Vocale (The Speaker)
const synth = window.speechSynthesis;
let voices = [];
let frenchVoices = [];
const voiceSelect = document.getElementById('voice-select');

function populateVoices() {
    voices = synth.getVoices();
    frenchVoices = voices.filter(v => v.lang.startsWith('fr'));
    
    if (frenchVoices.length > 0) {
        voiceSelect.innerHTML = '';
        frenchVoices.forEach((voice, index) => {
            const option = document.createElement('option');
            option.value = index;
            
            // Tentative de catégorisation (Google/Apple/Microsoft)
            let gender = "Mixte";
            const nameLower = voice.name.toLowerCase();
            
            // Voix masculines connues
            if (nameLower.includes('thomas') || nameLower.includes('paul') || nameLower.includes('nicolas') || nameLower.includes('mathieu') || nameLower.includes('claude')) {
                gender = "👨 Voix Homme";
            } 
            // Voix féminines connues
            else if (nameLower.includes('google') || nameLower.includes('aurélie') || nameLower.includes('audrey') || nameLower.includes('virginie') || nameLower.includes('hortense') || nameLower.includes('marguerite') || nameLower.includes('denise')) {
                gender = "👩 Voix Femme";
            } else {
                gender = "👩/👨 Auto";
            }
            
            option.textContent = `${gender} - ${voice.name}`;
            voiceSelect.appendChild(option);
        });
        
        // Présélectionner une voix féminine par défaut si possible
        const defaultVoiceIndex = frenchVoices.findIndex(v => v.name.toLowerCase().includes('google') || v.name.toLowerCase().includes('aurélie'));
        if (defaultVoiceIndex !== -1) {
            voiceSelect.selectedIndex = defaultVoiceIndex;
        }
    } else {
        voiceSelect.innerHTML = '<option value="">Voix Française par défaut</option>';
    }
}
// Chrome charge les voix en asynchrone
populateVoices();
if (speechSynthesis.onvoiceschanged !== undefined) {
    speechSynthesis.onvoiceschanged = populateVoices;
}

// 2. Initialiser la Reconnaissance Vocale (The Listener)
const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
if (!SpeechRecognition) {
    alert("Désolé, votre navigateur ne supporte pas la reconnaissance vocale. Utilisez Google Chrome sur Mac/PC.");
}
const recognition = new SpeechRecognition();
recognition.lang = 'fr-FR';
recognition.interimResults = false;
recognition.continuous = false; // On relance manuellement pour l'effet Talkie-Walkie

// --- Outils UI ---

function setOrbState(state, text) {
    orb.className = 'orb ' + state;
    statusText.innerText = text;
}

function addMessage(sender, text) {
    // Retirer le placeholder si besoin
    const placeholder = document.querySelector('.log-placeholder');
    if (placeholder) placeholder.remove();
    
    const div = document.createElement('div');
    div.className = 'msg ' + (sender === 'user' ? 'user' : 'ai');
    div.innerText = text;
    log.appendChild(div);
    log.scrollTop = log.scrollHeight; // Auto-scroll
}

// --- Logique du Flux Audio ---

// Quand le micro commence à écouter
recognition.onstart = () => {
    if (!isCalling) return;
    setOrbState('listening', 'Sophie vous écoute... (Parlez)');
};

// Quand le micro a capté une phrase entière
recognition.onresult = async (event) => {
    if (!isCalling) return;
    
    const userPhrase = event.results[0][0].transcript;
    addMessage('user', userPhrase);
    
    // Le micro se coupe, le cerveau (Backend/Gemini) réfléchit
    recognition.stop();
    setOrbState('thinking', 'Sophie réfléchit...');
    
    try {
        const response = await fetch('/api/chat', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ text: userPhrase, session_id: sessionId })
        });
        
        const data = await response.json();
        const aiResponseText = data.response;
        
        addMessage('ai', aiResponseText);
        
        // Sophie parle la réponse !
        speak(aiResponseText);
        
    } catch (error) {
        console.error("Erreur Backend:", error);
        setOrbState('idle', 'Erreur de connexion. Appel interrompu.');
        endCall();
    }
};

recognition.onerror = (event) => {
    console.error('Erreur Microphone:', event.error);
    if (!isCalling) return;
    
    if (event.error === 'no-speech') {
        // Personne n'a parlé, on relance doucement si on est pas en train de parler
        if (!isSpeaking) {
            try { recognition.start(); } catch(e){}
        }
    } else {
        setOrbState('idle', 'Erreur Micro. Veuillez autoriser l\'accès.');
        endCall();
    }
};

recognition.onend = () => {
    // Dès que le micro s'éteint, s'assurer de le rouvrir si on attend l'utilisateur
    // On ne le rouvre PAS si l'IA est en train de réfléchir ou de parler
    if (isCalling && !isSpeaking && orb.classList.contains('listening')) {
        try { recognition.start(); } catch(e){}
    }
};

// --- Synthèse Vocale ---
function speak(text) {
    if (!isCalling) return;
    
    synth.cancel(); // kill older speech
    
    const utterThis = new SpeechSynthesisUtterance(text);
    isSpeaking = true;
    setOrbState('speaking', 'Sophie parle...');
    
    // Appliquer la voix choisie par l'utilisateur
    const voiceIndex = document.getElementById('voice-select').value;
    if (voiceIndex !== "" && frenchVoices[voiceIndex]) {
        utterThis.voice = frenchVoices[voiceIndex];
    } else if (frenchVoices.length > 0) {
        utterThis.voice = frenchVoices[0]; // fallback
    }
    
    // LE RÉGLAGE DE LA VITESSE DEMANDÉ PAR L'UTILISATEUR !!
    utterThis.rate = 1.15; // Un peu plus rapide que la normale (1.0 = normal, 1.5 = rapide)
    utterThis.pitch = 1.0;
    
    utterThis.onend = () => {
        isSpeaking = false;
        if (isCalling) {
            // L'IA a fini de parler, c'est au tour de l'humain !
            setOrbState('listening', 'Sophie vous écoute...');
            // Petit délai pour éviter que le micro capte l'écho du haut parleur
            setTimeout(() => {
                try { recognition.start(); } catch(e){}
            }, 300);
        }
    };
    
    utterThis.onerror = () => {
        isSpeaking = false;
        console.error("Erreur de synthèse vocale.");
    };
    
    synth.speak(utterThis);
}

function startCall() {
    isCalling = true;
    callBtn.innerHTML = '<span class="icon">⏹</span> Raccrocher';
    callBtn.classList.add('active');
    
    const welcomeText = "Bonjour, vous êtes bien sur la ligne de l'Hôtel Madayann. Je suis Sophie. Comment puis-je vous aider aujourd'hui ?";
    addMessage('ai', welcomeText);
    speak(welcomeText);
}

function endCall() {
    isCalling = false;
    isSpeaking = false;
    callBtn.innerHTML = '<span class="icon">📞</span> Démarrer l\'Appel';
    callBtn.classList.remove('active');
    synth.cancel();         // Couper la parole
    recognition.stop();     // Couper l'écoute
    setOrbState('idle', 'Appel terminé.');
}

// Bouton principal
callBtn.addEventListener('click', () => {
    if (!isCalling) {
        startCall();
    } else {
        endCall();
    }
});
